public class EoLAgent {
	
	// This maps to the attribute role
	private String role;
	
	// This maps to the node value
	private String uri;

	public String getRole() {
		return role;
	}

	public String getURI() {
		return uri;
	}

	public void setURI(String uri) {
		this.uri = uri;
	}
	
	public void setRole(String role) {
		this.role = role;
	}
	

}
